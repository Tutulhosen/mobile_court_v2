                }
                if(selectCriminalJailInfo[i].lawSectionJail !=null){
                    lawSectionJailText=selectCriminalJailInfo[i].lawSectionJail;
                }else{
                    lawSectionJailText="";
                }

                html_crimminalpunishmentdiv += lawSectionJailText+additionalComma;

