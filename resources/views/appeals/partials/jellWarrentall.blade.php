    function one_jw_setParamsForcriminal(criminal, criminalID,jailInfo,prosecution) {
        var html_criminaldiv1 = "";
        var html_criminaldiv2 = "";

        var persinnumber = "";

        var html_criminal = "";
        var html_criminaldivdetails2 = "";
        var html_crimminalpunishmentdiv = "";
        var html_ext_jail_type = "";
        var flag = false;
        var jailcriminal=0;
        var selectCriminalJailInfo=[];

        for(var i=0;i<jailInfo.length;i++){
            if(parseInt(jailInfo[i].criminal_id)==parseInt(criminalID)){
                jailcriminal = jailInfo[i].criminal_id;
                selectCriminalJailInfo.push(jailInfo[i]);
            }
        }

        $.each(criminal, function (i, item) {
            if (parseInt(item.id) == parseInt(criminalID) &&parseInt(item.id) ==parseInt(jailcriminal)) {
                flag = true;
                html_criminaldiv2 += "<span>" + item.name_bng;
                html_criminaldiv1 += "<span>" + item.name_bng;
                persinnumber = "আসামি ";

                html_criminaldiv2 += "</span>&nbsp;";

                var occupation = item.occupation ? item.occupation : "";
                var age = item.age ? item.age : "";

                html_criminaldivdetails2 += "<span> " + item.name_bng +
                    ",&nbsp; " + item.custodian_type + "&nbsp;:&nbsp;" + item.custodian_name +
                    ",&nbsp;বয়স - আনুমানিক:&nbsp;" + age + "&nbsp;বছর&nbsp;" +
                    ",&nbsp;পেশা-&nbsp;:&nbsp;" + occupation +
                    ",&nbsp;ঠিকানা (পূর্ণাঙ্গ)&nbsp;:&nbsp;" + item.permanent_address;

                html_criminaldivdetails2 += "</span> &nbsp;";

            }

        });


        var repText="";
        var repWarrent="";
        var ext_jail_type_str="";
        var additionalComma=',';
        var lawSectionJailText='';
        for(var i=0;i<selectCriminalJailInfo.length;i++){

                if(selectCriminalJailInfo.length==(i+1)){
                    additionalComma="";
                }
                if(selectCriminalJailInfo[i].receipt_no != null){
                    if (jailInfo[i].rep_warrent_duration){
                        repText = " এবং অর্থদণ্ড অনাদায়ে " ;
                        repWarrent=jailInfo[i].rep_warrent_duration;
                    }
                }

                if(selectCriminalJailInfo[i].exe_jail_type==1){
                    ext_jail_type_str="উক্ত কারাদণ্ডসমূহ একটির পর একটি কার্যকর হবে";
